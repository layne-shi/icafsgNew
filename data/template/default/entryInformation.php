                </p>
                <p class="MsoNormal" align="left">
                    <b>◆參賽相關：</b>1、所有參賽選手須服從“國際音樂舞蹈交流促進會”的賽事安排，閱讀此須知的選手，證明可完全遵守“國際音樂舞蹈交流促進會”的相關要求；2、選手只能選擇一個項目參賽，<b>選手只能在高於自身組別或以</b><b>不同參賽方向、專業、參賽</b><b>形式參賽時，方可兼報</b>；3、組委會將根據各選手的年齡段及專業的不同進行合理分組，並在賽前出示分組名單及具體賽程安排；4、賽後選手及陪同家長必須聽從組委會指定領隊老師的安排，未經允許不可隨意離開場地；5、比賽時請不要大聲喧嘩，如未遵守比賽要求，組委會可視情況取消選手參賽資格。<b></b>
                </p>
                <p class="MsoNormal" align="left" style="">
                    <b>◆出場順序：</b>1、比賽前，組委會將根據參賽選手情況對所有組別選手進行電腦隨機抽籤；2、組委會有權因組織原因、組別或曲目原因更改出場順序，如選手身體不適或水土不服,經領隊與組委會商議許可後可在最後出場比賽。<b></b>
                </p>
                <p class="MsoNormal" align="left" style="">
                    <b>◆參賽專業：</b>I聲樂大賽：美聲、民族、通俗、童聲、合唱、小組唱；II鋼琴大賽：鋼琴、雙排鍵、電子管風琴；III 民族器樂大賽：琵琶、二胡、笛子、揚琴、古箏、 柳琴、中阮、笙、嗩呐、三弦、打擊樂等；IV管弦樂大賽：管樂比賽—銅管類、木管類；弦樂比賽—弦樂類；打擊樂等；V舞蹈大賽：中國舞—民族民間舞、中國古典舞；芭蕾舞—現代芭蕾舞、古典芭蕾舞；現代舞—各類題材現代舞；國際標準舞—標準舞、拉丁美洲舞、含隊型舞；青少兒舞蹈—青少年舞蹈、幼兒舞蹈等；
                </p>
                <p class="MsoNormal" align="left" style="">
                    <b>◆參賽形式：</b>器樂、聲樂類：個人單項、重奏/重唱、大中小型樂團、合唱團、小組唱；舞蹈類：獨舞、雙人舞、三人舞、群舞。
                </p>
                <p class="MsoNormal" align="left">
                    <b>◆比賽分組：</b>1、成人組（包括職業、非職業、研究生）；2、高等院校組（包括藝術院校、師範院校、普通院校）；3、高中組（包括普通高中組和藝術高中組）；4、初中組（包括普通初中組和藝術初中組）；5、藝術中專組(包括1-2年級組、3年級以上組)；6、小學組（包括小學低、中、高年級組）；7、幼兒組（學齡前）。<b></b>
                </p>
                <p class="MsoNormal">
                    <b>◆曲目</b><b>/</b><b>劇目要求：</b>聲樂類：1、個人單項、重唱：需演唱一首與參賽本人或重唱組合專業程度相符的樂曲；合唱：演唱兩首作品（其中一首為中國曲目）。器樂類：1、個人單項、重奏：比賽均需演奏一首與參賽本人或重奏組合專業程度相符的樂曲，比賽時要求背譜；2、大中小型樂團：比賽需演奏兩首與本樂團專業程度相符的樂曲；3、舞蹈類：中國舞、現代舞選手須表演一個與參賽選手本人或團體專業程度相符的劇目，專業組選手須另行準備一分鐘基本功展示（音樂自備（MP3格式））；芭蕾舞選手須表演一個原創劇目和一段古典芭蕾變奏；芭蕾舞選手、國際標準舞選手無須進行基本功展示；組委會依據國際舞蹈大賽慣例，不提供燈光舞美，音響以比賽現場所呈現的實際效果為准；國際標準舞選手可參加兩個不同小舞種比賽，視為兼報，申請選手需另行繳納兼報評審費。<span></span>
                </p>
                <p class="MsoNormal">
                    <b>◆時間要求：</b>器樂類:【個人單項、重奏】曲目要求在3分30秒與5分之間，請選手酌情選擇參賽曲目；【大中小型民族樂團】總時間控制在10分鐘內完成；聲樂類：參賽曲目控制在五分鐘內。舞蹈類:獨舞、雙人舞、三人舞作品時間不得超過5分鐘；古典芭蕾變奏可根據原創劇目時間表演;群舞時間不得超過8分鐘為原則。
                註：具體時間要求詳見官網細則。如演奏曲目過短或超時，參評評委將會根據選手所在分組及相關情況扣除相應分數，並有權叫停，請選手酌情選擇參賽曲目/劇目。&nbsp;
                </p>
